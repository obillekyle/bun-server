export const cleanSQLQuotes = (sql: string) =>
  sql.replace(/`([^`]+)`/g, (match, word) =>
    !sqlKeywords.has(word.toUpperCase()) &&
    /^[a-zA-Z_][a-zA-Z0-9_]*$/.test(word)
      ? word
      : match,
  )
